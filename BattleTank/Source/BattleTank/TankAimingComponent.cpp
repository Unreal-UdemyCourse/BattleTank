void UTankAimingComponent::Initialize(UTankBarrel* BarrelToSet, UTankTurret* TurretToSet)
{
	if (!ensure(BarrelToSet)) { return; }
	Barrel = BarrelToSet;

	if (!ensure(TurretToSet)) { return; }
	Turret = TurretToSet;
}

